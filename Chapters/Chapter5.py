#List

num =[1,4,5,8,9,10]
#index
index = num.index(4,1,4)
print(index)

#pop
num.pop(2)

#remove
num.remove(10)
print(num)

#insert
num.insert(2,90)
print(num)

#append
num.append(100)
print(num)

#extend
num.extend([200,300])
print(num)

#Count
print(num.count(200))

#reverse
num.reverse()
print(num)

#copy
print(num.copy())

#sort
num.sort()
print(num)




